/**
 * @brief Account statement data structure
 */
struct AccountStatementData {
    trading::UnifiedAccount account;
    std::vector<trading::UnifiedPosition> positions;
    std::vector<trading::UnifiedOrder> orders;
    std::vector<tax::RealizedGainLoss> realized_gains;
    
    double beginning_value{0};
    double ending_value{0};
    double deposits{0};
    double withdrawals{0};
    double dividends{0};
    double interest{0};
    double fees{0};
    double realized_gain_loss{0};
    double unrealized_gain_loss{0};
};

/**
 * @brief Generate account statement HTML
 */
inline std::string generate_account_statement_html(
    const AccountStatementData& data,
    const ExtendedReportConfig& config) {
    
    EnhancedHTMLBuilder html;
    
    ExtendedReportConfig cfg = config;
    if (cfg.title.empty()) cfg.title = "Account Statement";
    if (cfg.account_name.empty()) cfg.account_name = data.account.name;
    if (cfg.account_id.empty()) cfg.account_id = data.account.broker_account_id;
    
    html.start_document(cfg.title);
    html.add_header(cfg);
    
    // Account Summary
    html.start_section("Account Summary");
    html.start_metric_grid();
    html.add_metric("Portfolio Value", fmt_currency(data.account.portfolio_value));
    html.add_metric("Cash Balance", fmt_currency(data.account.cash));
    html.add_metric("Buying Power", fmt_currency(data.account.buying_power));
    html.add_metric("Margin Used", fmt_currency(data.account.margin_used));
    html.end_metric_grid();
    html.end_section();
    
    // Period Activity
    html.start_section("Period Activity");
    html.start_table({"Description", "Amount"});
    html.add_table_row({"Beginning Value", fmt_currency(data.beginning_value)}, {false, true});
    html.add_table_row({"Deposits", fmt_currency(data.deposits)}, {false, true});
    html.add_table_row({"Withdrawals", fmt_currency(-data.withdrawals)}, {false, true});
    html.add_table_row({"Dividends", fmt_currency(data.dividends)}, {false, true});
    html.add_table_row({"Interest", fmt_currency(data.interest)}, {false, true});
    html.add_table_row({"Fees", fmt_currency(-data.fees)}, {false, true});
    html.add_table_row({"Realized Gain/Loss", fmt_currency(data.realized_gain_loss)}, {false, true});
    html.add_table_row({"Unrealized Gain/Loss", fmt_currency(data.unrealized_gain_loss)}, {false, true});
    html.add_table_row({"<strong>Ending Value</strong>", 
                       "<strong>" + fmt_currency(data.ending_value) + "</strong>"}, {false, true});
    html.end_table();
    html.end_section();
    
    // Holdings
    if (!data.positions.empty()) {
        html.start_section("Current Holdings");
        html.start_table({"Symbol", "Qty", "Avg Cost", "Price", "Value", "Gain/Loss", "%"});
        
        double total_value = 0, total_gain = 0;
        for (const auto& pos : data.positions) {
            total_value += pos.market_value;
            total_gain += pos.unrealized_pl;
            
            html.add_table_row({
                pos.symbol,
                fmt_number(pos.qty, pos.qty == std::floor(pos.qty) ? 0 : 4),
                fmt_currency(pos.avg_entry_price),
                fmt_currency(pos.current_price),
                fmt_currency(pos.market_value),
                fmt_currency(pos.unrealized_pl),
                fmt_percent(pos.unrealized_pl_pct)
            }, {false, true, true, true, true, true, true});
        }
        
        html.add_table_row({
            "<strong>Total</strong>", "", "", "",
            "<strong>" + fmt_currency(total_value) + "</strong>",
            "<strong>" + fmt_currency(total_gain) + "</strong>", ""
        }, {false, false, false, false, true, true, false});
        
        html.end_table();
        html.end_section();
    }
    
    // Orders
    if (!data.orders.empty()) {
        html.start_section("Recent Orders");
        html.start_table({"Date", "Symbol", "Side", "Type", "Qty", "Price", "Status"});
        
        int count = 0;
        for (const auto& order : data.orders) {
            if (++count > 50) break;
            
            html.add_table_row({
                order.created_at.substr(0, 10),
                order.symbol,
                trading::order_side_to_string(order.side),
                trading::order_type_to_string(order.type),
                fmt_number(order.qty, 0),
                order.filled_avg_price > 0 ? fmt_currency(order.filled_avg_price) :
                    (order.limit_price > 0 ? fmt_currency(order.limit_price) : "MKT"),
                trading::order_status_to_string(order.status)
            }, {false, false, false, false, true, true, false});
        }
        
        html.end_table();
        html.end_section();
    }
    
    html.add_footer(cfg.footer_text);
    html.end_document();
    
    return html.build();
}

