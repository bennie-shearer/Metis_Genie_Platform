/**
 * @brief Write report content to file
 */
inline bool write_report_file(const std::string& content, const std::string& filepath) {
    try {
        std::ofstream file(filepath);
        if (!file.is_open()) return false;
        file << content;
        return file.good();
    } catch (...) {
        return false;
    }
}

