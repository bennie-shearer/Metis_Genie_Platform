/**
 * @brief Performance report data structure
 */
struct PerformanceReportData {
    double total_return_pct{0};
    double annualized_return{0};
    double ytd_return{0};
    double mtd_return{0};
    
    double sharpe_ratio{0};
    double sortino_ratio{0};
    double calmar_ratio{0};
    double information_ratio{0};
    
    double volatility{0};
    double max_drawdown{0};
    double beta{0};
    double alpha{0};
    double tracking_error{0};
    
    double win_rate{0};
    int total_trades{0};
    int winning_trades{0};
    int losing_trades{0};
    double avg_win{0};
    double avg_loss{0};
    double profit_factor{0};
    
    std::string benchmark_name{"S&P 500"};
    double benchmark_return{0};
    
    std::map<std::string, double> monthly_returns;
    std::map<std::string, double> sector_allocation;
};

/**
 * @brief Generate performance report HTML
 */
inline std::string generate_performance_report_html(
    const PerformanceReportData& data,
    const ExtendedReportConfig& config) {
    
    EnhancedHTMLBuilder html;
    
    ExtendedReportConfig cfg = config;
    if (cfg.title.empty()) cfg.title = "Performance Report";
    
    html.start_document(cfg.title);
    html.add_header(cfg);
    
    // Key Metrics
    html.start_section("Performance Summary");
    html.start_metric_grid();
    html.add_metric("Total Return", fmt_percent(data.total_return_pct),
                   data.total_return_pct >= 0 ? "positive" : "negative");
    html.add_metric("Annualized", fmt_percent(data.annualized_return));
    html.add_metric("YTD Return", fmt_percent(data.ytd_return));
    html.add_metric("MTD Return", fmt_percent(data.mtd_return));
    html.end_metric_grid();
    html.end_section();
    
    // Risk-Adjusted
    html.start_section("Risk-Adjusted Returns");
    html.start_table({"Metric", "Value", "Description"});
    html.add_table_row({"Sharpe Ratio", fmt_number(data.sharpe_ratio),
                       "Risk-adjusted return (higher is better)"}, {false, true, false});
    html.add_table_row({"Sortino Ratio", fmt_number(data.sortino_ratio),
                       "Downside risk-adjusted return"}, {false, true, false});
    html.add_table_row({"Calmar Ratio", fmt_number(data.calmar_ratio),
                       "Return / Max Drawdown"}, {false, true, false});
    html.add_table_row({"Information Ratio", fmt_number(data.information_ratio),
                       "Active return per tracking error"}, {false, true, false});
    html.end_table();
    html.end_section();
    
    // Risk Metrics
    html.start_section("Risk Analysis");
    html.start_metric_grid();
    html.add_metric("Volatility", fmt_percent(data.volatility));
    html.add_metric("Max Drawdown", fmt_percent(data.max_drawdown), "negative");
    html.add_metric("Beta", fmt_number(data.beta));
    html.add_metric("Alpha", fmt_percent(data.alpha));
    html.end_metric_grid();
    html.end_section();
    
    // Benchmark Comparison
    html.start_section("Benchmark Comparison");
    double excess = data.total_return_pct - data.benchmark_return;
    html.start_table({"Metric", "Portfolio", data.benchmark_name, "Excess"});
    html.add_table_row({"Total Return", fmt_percent(data.total_return_pct),
                       fmt_percent(data.benchmark_return), fmt_percent(excess)},
                      {false, true, true, true});
    html.add_table_row({"Tracking Error", fmt_percent(data.tracking_error), "-", "-"},
                      {false, true, false, false});
    html.end_table();
    html.end_section();
    
    // Trading Stats
    html.start_section("Trading Statistics");
    html.start_table({"Metric", "Value"});
    html.add_table_row({"Total Trades", std::to_string(data.total_trades)}, {false, true});
    html.add_table_row({"Winning Trades", std::to_string(data.winning_trades)}, {false, true});
    html.add_table_row({"Losing Trades", std::to_string(data.losing_trades)}, {false, true});
    html.add_table_row({"Win Rate", fmt_percent(data.win_rate)}, {false, true});
    html.add_table_row({"Average Win", fmt_currency(data.avg_win)}, {false, true});
    html.add_table_row({"Average Loss", fmt_currency(data.avg_loss)}, {false, true});
    html.add_table_row({"Profit Factor", fmt_number(data.profit_factor)}, {false, true});
    html.end_table();
    html.end_section();
    
    // Monthly Returns
    if (!data.monthly_returns.empty()) {
        html.add_page_break();
        html.start_section("Monthly Returns");
        
        std::map<int, std::map<int, double>> yearly;
        for (const auto& [month, ret] : data.monthly_returns) {
            int year = std::stoi(month.substr(0, 4));
            int mon = std::stoi(month.substr(5, 2));
            yearly[year][mon] = ret;
        }
        
        html.start_table({"Year", "Jan", "Feb", "Mar", "Apr", "May", "Jun",
                         "Jul", "Aug", "Sep", "Oct", "Nov", "Dec", "YTD"});
        
        for (const auto& [year, months] : yearly) {
            std::vector<std::string> row = {std::to_string(year)};
            double total = 0;
            
            for (int m = 1; m <= 12; ++m) {
                auto it = months.find(m);
                if (it != months.end()) {
                    row.push_back(fmt_percent(it->second));
                    total += it->second;
                } else {
                    row.push_back("-");
                }
            }
            row.push_back(fmt_percent(total));
            
            std::vector<bool> numeric(14, true);
            numeric[0] = false;
            html.add_table_row(row, numeric);
        }
        
        html.end_table();
        html.end_section();
    }
    
    html.add_footer(cfg.footer_text);
    html.end_document();
    
    return html.build();
}

