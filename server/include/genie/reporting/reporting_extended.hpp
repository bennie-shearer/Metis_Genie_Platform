/**
 * @brief Risk report data structure
 */
struct RiskReportData {
    double var_95_1d{0};
    double var_99_1d{0};
    double var_95_10d{0};
    double cvar_95{0};
    
    struct PositionRisk {
        std::string symbol;
        double market_value{0};
        double weight{0};
        double volatility{0};
        double beta{0};
        double var_contribution{0};
        double marginal_var{0};
    };
    std::vector<PositionRisk> position_risks;
    
    double top_5_concentration{0};
    double herfindahl_index{0};
    
    struct StressScenario {
        std::string name;
        double impact{0};
        std::string description;
    };
    std::vector<StressScenario> stress_tests;
    
    struct RiskLimit {
        std::string metric;
        double current{0};
        double limit{0};
        bool breached{false};
    };
    std::vector<RiskLimit> risk_limits;
};

/**
 * @brief Generate risk report HTML
 */
inline std::string generate_risk_report_html(
    const RiskReportData& data,
    const ExtendedReportConfig& config) {
    
    EnhancedHTMLBuilder html;
    
    ExtendedReportConfig cfg = config;
    cfg.title = "Portfolio Risk Report";
    
    html.start_document(cfg.title);
    html.add_header(cfg);
    
    // VaR Summary
    html.start_section("Value at Risk (VaR)");
    html.start_metric_grid();
    html.add_metric("1-Day VaR (95%)", fmt_currency(data.var_95_1d), "negative");
    html.add_metric("1-Day VaR (99%)", fmt_currency(data.var_99_1d), "negative");
    html.add_metric("10-Day VaR (95%)", fmt_currency(data.var_95_10d), "negative");
    html.add_metric("CVaR / ES (95%)", fmt_currency(data.cvar_95), "negative");
    html.end_metric_grid();
    
    html.add_paragraph(
        "Value at Risk represents the maximum expected loss at the given confidence level. "
        "CVaR (Conditional VaR) shows the expected loss when losses exceed VaR."
    );
    html.end_section();
    
    // Position Risk
    if (!data.position_risks.empty()) {
        html.start_section("Position Risk Analysis");
        html.start_table({"Symbol", "Value", "Weight", "Vol", "Beta", "VaR Contrib", "Marginal"});
        
        for (const auto& pr : data.position_risks) {
            html.add_table_row({
                pr.symbol,
                fmt_currency(pr.market_value),
                fmt_percent(pr.weight),
                fmt_percent(pr.volatility),
                fmt_number(pr.beta),
                fmt_currency(pr.var_contribution),
                fmt_currency(pr.marginal_var)
            }, {false, true, true, true, true, true, true});
        }
        
        html.end_table();
        html.end_section();
    }
    
    // Concentration
    html.start_section("Concentration Risk");
    html.start_table({"Metric", "Value", "Assessment"});
    
    std::string conc_assess = data.top_5_concentration > 50 ?
        "<span class='warning'>High - Consider diversifying</span>" : "Acceptable";
    html.add_table_row({"Top 5 Concentration", fmt_percent(data.top_5_concentration),
                       conc_assess}, {false, true, false});
    
    std::string hhi_assess = data.herfindahl_index > 0.25 ?
        "<span class='warning'>Concentrated</span>" :
        (data.herfindahl_index > 0.15 ? "Moderate" : "Diversified");
    html.add_table_row({"HHI Index", fmt_number(data.herfindahl_index), hhi_assess},
                      {false, true, false});
    
    html.end_table();
    html.end_section();
    
    // Stress Tests
    if (!data.stress_tests.empty()) {
        html.start_section("Stress Test Scenarios");
        html.start_table({"Scenario", "Impact", "Description"});
        
        for (const auto& st : data.stress_tests) {
            html.add_table_row({st.name, fmt_currency(st.impact), st.description},
                              {false, true, false});
        }
        
        html.end_table();
        html.end_section();
    }
    
    // Risk Limits
    if (!data.risk_limits.empty()) {
        html.start_section("Risk Limit Monitoring");
        html.start_table({"Metric", "Current", "Limit", "Status"});
        
        for (const auto& rl : data.risk_limits) {
            std::string status = rl.breached ?
                "<span class='badge badge-danger'>BREACHED</span>" :
                "<span class='badge badge-success'>OK</span>";
            
            html.add_table_row({rl.metric, fmt_number(rl.current), fmt_number(rl.limit), status},
                              {false, true, true, false});
        }
        
        html.end_table();
        html.end_section();
    }
    
    html.add_footer(cfg.footer_text);
    html.end_document();
    
    return html.build();
}

